export async function registerRoutes(app: FastifyInstance) {
  await app.register(healthRoutes);
  await app.register(authRoutes, { prefix: "/api/auth" });
  await app.register(projectsRoutes, { prefix: "/api/projects" });
  await app.register(projectDetailRoutes, { prefix: "/api/projects" });
  await app.register(milestonesRoutes, { prefix: "/api/projects" });
  await app.register(risksRoutes, { prefix: "/api/projects" });
  await app.register(issuesRoutes, { prefix: "/api/projects" });
  await app.register(changeRequestsRoutes, { prefix: "/api/projects" });
  await app.register(consultantsRoutes, { prefix: "/api/consultants" });
  await app.register(blocksRoutes, { prefix: "/api/consultants" });
  await app.register(timeEntriesRoutes, { prefix: "/api/time-entries" });
  await app.register(expensesRoutes, { prefix: "/api/expenses" });
  await app.register(forecastsRoutes, { prefix: "/api/forecasts" });
  await app.register(statsRoutes, { prefix: "/api/stats" });
  await app.register(adminUsersRoutes, { prefix: "/api/admin/users" });
  await app.register(fxRoutes, { prefix: "/api/fx" });
  await app.register(revenueRoutes, { prefix: "/api/revenue" });
  await app.register(assignmentsRoutes, { prefix: "/api/assignments" });
  await app.register(capacityRoutes, { prefix: "/api/capacity" });
  await app.register(snapshotsRoutes, { prefix: "/api/snapshots" });
  await app.register(alertsRoutes, { prefix: "/api/alerts" });
  await app.register(auditRoutes, { prefix: "/api/audit" });
}
